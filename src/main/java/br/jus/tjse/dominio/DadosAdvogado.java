package br.jus.tjse.dominio;

public class DadosAdvogado {
	
	private String codOab;
	private String letraOab;
	private String ufOab;
	private String nomeAdvogado;

	public String getCodOab() {
		return codOab;
	}
	public void setCodOab(String codOab) {
		this.codOab = codOab;
	}
	public String getLetraOab() {
		return letraOab;
	}
	public void setLetraOab(String letraOab) {
		this.letraOab = letraOab;
	}
	public String getUfOab() {
		return ufOab;
	}
	public void setUfOab(String ufOab) {
		this.ufOab = ufOab;
	}
	public String getNomeAdvogado() {
		return nomeAdvogado;
	}
	public void setNomeAdvogado(String nomeAdvogado) {
		this.nomeAdvogado = nomeAdvogado;
	}

}
